drop table if exists authentication;
drop table if exists account;

CREATE TABLE account
(
  account_id bigserial NOT NULL,
  account_name text NOT NULL,
  role text NOT NULL,
  CONSTRAINT account_pkey PRIMARY KEY (account_id)
);

CREATE TABLE authentication
(
  account_id bigint NOT NULL,
  passphrase text,
  CONSTRAINT authentication_pkey PRIMARY KEY (account_id),
  CONSTRAINT authentication_account_id_fkey FOREIGN KEY (account_id)
      REFERENCES account (account_id) MATCH SIMPLE
      ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE public.colloquial
(
    colloquial_id bigserial PRIMARY KEY,
    colloquial_name text NOT NULL,
    colloquial_part int NOT NULL,
    colloquial_category text NOT NULL
);


